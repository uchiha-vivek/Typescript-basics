// ===========================
// Type Annotations in TypeScript
// ===========================

// Defining a variable with string type
const name: string = 'vivek'; // 'name' holds a string value

// Defining a variable with number type
const marks: number = 25; // 'marks' holds a numeric value

// Defining a variable with boolean type
const isAdmin: boolean = true; // 'isAdmin' holds a boolean value indicating admin status

// Logging the values to the console
console.log(name);     // Output: vivek
console.log(marks);    // Output: 25
console.log(isAdmin);  // Output: true

